/// Request parser for `Api`.
pub struct ApiRequestParser;
impl RequestParser for ApiRequestParser {
    fn parse_operation_id(request: &Request) -> Result<&'static str, ()> {
        let path = paths::GLOBAL_REGEX_SET.matches(request.uri().path());
        match request.method() {

            // Authenticate - POST /authenticate
            &hyper::Method::Post if path.matched(paths::ID_AUTHENTICATE) => Ok("Authenticate"),

            // Invalidate - POST /invalidate
            &hyper::Method::Post if path.matched(paths::ID_INVALIDATE) => Ok("Invalidate"),

            // Refresh - POST /refresh
            &hyper::Method::Post if path.matched(paths::ID_REFRESH) => Ok("Refresh"),

            // Siginout - POST /signout
            &hyper::Method::Post if path.matched(paths::ID_SIGNOUT) => Ok("Siginout"),

            // Validate - POST /validate
            &hyper::Method::Post if path.matched(paths::ID_VALIDATE) => Ok("Validate"),
            _ => Err(()),
        }
    }
}
